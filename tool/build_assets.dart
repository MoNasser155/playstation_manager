  code.writeln('/// Application image paths');
  code.writeln('class AppImages {');
  code.writeln('  AppImages._();');
  code.writeln();

  if (imagesDir.existsSync()) {
    final imageFiles = imagesDir.listSync().whereType<File>().where((file) {
      final ext = file.path.split('.').last.toLowerCase();
      return ['png', 'jpg', 'jpeg', 'webp', 'gif', 'bmp'].contains(ext);
    }).toList()..sort((a, b) => a.path.compareTo(b.path));

    if (imageFiles.isNotEmpty) {
      for (var file in imageFiles) {
        final fileName = file.uri.pathSegments.last;
        final imageName = _toCamelCase(fileName.split('.').first);
        final path = 'assets/images/$fileName';

        code.writeln('  /// $fileName');
        code.writeln('  static const String $imageName = \'$path\';');
        code.writeln();
      }

      print('  ✓ Generated ${imageFiles.length} image(s)');
    } else {
      code.writeln('  // No images found');
      code.writeln();
      print('  ⚠️  No images found in assets/images');
    }
  } else {
    code.writeln('  // assets/images directory not found');
    code.writeln();
    print('  ⚠️  assets/images directory not found');
  }

  code.writeln('}');
}

